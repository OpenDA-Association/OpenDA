      ! *** DSLLC UTIL
      FUNCTION PARSE_REAL(INLINE)

        CHARACTER*(*) INLINE
        CHARACTER*15  CVAL,TMPVAL
        
        ILEN=LEN_TRIM(INLINE)
        PARSE_REAL=0.
        DO IC=1,ILEN
          IF(INLINE(IC:IC).EQ.':')THEN
            DO IPOS=IC+1,ILEN
              IF(INLINE(IPOS:IPOS).NE.' ')EXIT
            ENDDO
            IF(IPOS.GT.ILEN)RETURN
            CVAL=INLINE(IPOS:ILEN)
            ILEN2=LEN_TRIM(CVAL)
            DO JC=1,ILEN2
              IF(CVAL(JC:JC).EQ.' '.OR.CVAL(JC:JC).EQ.','
     &             .OR.JC.EQ.ILEN2)THEN
                READ(CVAL(1:JC),'(F12.1)',ERR=999)PARSE_REAL
                RETURN
              ENDIF
            ENDDO
          ENDIF
        ENDDO
  
  999   print *, ' error parsing real'
        RETURN
      END

      FUNCTION PARSE_LOGICAL(INLINE)

        CHARACTER*(*) INLINE
        CHARACTER*12  CVAL
        LOGICAL       PARSE_LOGICAL
        
        ILEN=LEN_TRIM(INLINE)
        DO IC=1,ILEN
          IF(INLINE(IC:IC).EQ.':')THEN
            DO IPOS=IC+1,ILEN
              IF(INLINE(IPOS:IPOS).NE.' ')EXIT
            ENDDO
            IF(IPOS.GT.ILEN)RETURN
            CVAL=INLINE(IPOS:ILEN)
            ILEN2=LEN_TRIM(CVAL)
            DO JC=1,ILEN2
              IF(CVAL(JC:JC).EQ.' '.OR.CVAL(JC:JC).EQ.','
     &             .OR.JC.EQ.ILEN2)THEN
                IF(CVAL(1:1).EQ.'T'.OR.CVAL(1:1).EQ.'Y')THEN
                  PARSE_LOGICAL=.TRUE.
                  RETURN
                ENDIF
              ENDIF
            ENDDO
          ENDIF
        ENDDO
        PARSE_LOGICAL=.FALSE.
  900   FORMAT(L1)  
  999   RETURN
      END
