module cta_f90_flush_mod

  implicit none

  public

  !  \brief Flushes STDOUT and STDERR both for C as FORTRAN
  ! 
  !   \note Function is introduced because some FORTRAN compilers use
  !   different flushing than the C compiler. As a result the order of output
  !   generated by both languages is "random". This routine makes sure that
  !   all buffers have been flushed.
  ! 
  !
  interface CTA_F90_Flush
    subroutine CTA_Flush(  )
    end subroutine CTA_Flush
  end interface


end module cta_f90_flush_mod

